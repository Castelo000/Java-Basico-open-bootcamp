package com.tarea.openbootcamp;

public class Tipos {

    public static void main(String[] args) {

        System.out.println("Tipos de datos:");
        // 1 numericos

        System.out.println("numericos");

        System.out.println("enteros:");

        // 1.1 enteros
        byte bytevariable1= 5;
        short variable2 = 10;
        int variable3 = 30;
        long variable4 = 100;
        long variable5 = 381609685;
        System.out.println(bytevariable1);

        System.out.println("decimales:");

        // 1.2 decimales
        float variable6= 5.5f;
        double doublevariable7= 10.5d;
        double variable8 = 49.99;

        System.out.println(doublevariable7);

        System.out.println("booleano:");
        // 2 booleano
        boolean booleanvariable9 = false;
        boolean variable10= true;

        System.out.println(booleanvariable9);

        System.out.println("texto:");
        // 3 texto
        char variable11 = 'a';
        String stringvariable12 = "Lorem ipsum dolor sit amet.....";

        System.out.println(stringvariable12);

    }

}


